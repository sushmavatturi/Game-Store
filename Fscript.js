function openGame(game) {
    if (game === 'tic-tac-toe') {
      window.location.href = 'Tic Tac Toe/tic-tac-toe.html'; 
    } else if (game === 'typing-speed') {
      window.location.href = 'Typing Speed/typing-speed.html'; 
    }
    else if(game=='sudoku'){
      window.location.href = 'Sudoku-Solver/index.html'; 

    }
  }
  